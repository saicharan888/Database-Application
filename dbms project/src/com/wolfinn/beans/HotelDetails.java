package com.wolfinn.beans;

public class HotelDetails {
	
	public String hotel_id;
	public String hotel_name;
	public String hotel_phone_no;
	public String city;
	public String manager_Staff_id;
	public String street_name;
	public String getHotel_id() {
		return hotel_id;
	}
	public void setHotel_id(String hotel_id) {
		this.hotel_id = hotel_id;
	}
	public String getHotel_name() {
		return hotel_name;
	}
	public void setHotel_name(String hotel_name) {
		this.hotel_name = hotel_name;
	}
	public String getHotel_phone_no() {
		return hotel_phone_no;
	}
	public void setHotel_phone_no(String hotel_phone_no) {
		this.hotel_phone_no = hotel_phone_no;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getManager_Staff_id() {
		return manager_Staff_id;
	}
	public void setManager_Staff_id(String manager_Staff_id) {
		this.manager_Staff_id = manager_Staff_id;
	}
	public String getStreet_name() {
		return street_name;
	}
	public void setStreet_name(String street_name) {
		this.street_name = street_name;
	}
	

	
	

}
